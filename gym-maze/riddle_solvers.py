def binary_to_number(binary):
    number = 0
    length = len(binary)
    for i in range(0, length):
        if (binary[i] == '1'):
            number += 2**(length-i-1)
    return number


def base64_padding(s):
    return s + '=' * (4 - len(s) % 4)


